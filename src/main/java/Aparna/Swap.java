package Aparna;

public class Swap
{

	public static void main(String[] args)
	{
		int a=100;
		int b=20;
		System.out.println("Before Swapping");
		System.out.println("The value of a:"+a);	
		System.out.println("The value of b:"+b);	
		int c=a;
		a=b;
		b=c;
		System.out.println("After Swapping");
	System.out.println("The value of a:"+a);	
	System.out.println("The value of b:"+b);	

	}

}
